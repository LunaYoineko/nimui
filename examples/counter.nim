## =============================================================================
## examples/counter.nim
## Nimui のサンプルアプリケーション: カウンター
##
## このサンプルは以下の機能をデモしている:
##   - Appの生成とキーイベント登録
##   - Widget ツリーの構築 (vbox, header, center, footer, label, progress)
##   - 外部変数の変更が build() 経由で自動的に画面に反映される仕組み
##
## 操作方法:
##   SPACE キー: カウンターを1加算
##   Q キー:     アプリケーションを終了
## =============================================================================

import std/asyncdispatch
import nimui  # nimui ライブラリをインポート

# =============================================================================
# アプリケーションの状態
# =============================================================================

## カウンターの値 (build() 内で参照される)
var count = 0

## Appインスタンスを生成
let app = newApp()

# =============================================================================
# ウィジェットツリーの構築
# =============================================================================

## 毎フレーム呼ばれる build 関数
## 戻り値: 画面に描画する Widget ツリー
##
## 画面構成:
##   ┌─────────────────────────────────┐
##   │ 🚀 Nimui v0.1.0 (ヘッダー)      │  ← header: 全幅バー
##   ├─────────────────────────────────┤
##   │                                 │
##   │   [ Counter Widget ]            │
##   │                                 │  ← center: 中央寄せボックス
##   │   Press SPACE to increment.     │     (40x10)
##   │   Current Count: 5              │
##   │   ████████░░░░░░░░░░░░          │  ← progress: プログレスバー
##   │                                 │
##   ├─────────────────────────────────┤
##   │ Q: Quit | SPACE: Increment      │  ← footer: 全幅バー
##   └─────────────────────────────────┘
##
## レイアウト:
##   vbox (縦並び)
##     ├─ header  (1行、全幅)
##     ├─ center  (40文字x10行、画面中央)
##     │   └─ 複数のlabel + progress を縦に積む
##     └─ footer  (1行、全幅)
##
## 注意: count 変数は build() 内で直接参照しているため、
##       onKey ハンドラで count を変更すると、
##       次のフレームの build() 呼び出しで自動的に新しい値が反映される
proc build(): Widget =
  vbox(
    # ヘッダーバー: アプリ名を白文字・青背景で表示
    header("Nimui v0.1.0", fg = colWhite, bg = colBlue, bold = true),

    # 中央寄せボックス (幅40、高さ10)
    center(40, 10,
      # タイトル (紫・太字)
      label(" [ Counter Widget ] ", fg = colPurple, bold = true),
      label(""),  # 空行
      # 説明テキスト
      label("Press SPACE to increment counter.", fg = colText),
      label(""),  # 空行
      # 現在のカウンター値 (緑・太字)
      label("Current Count: " & $count, fg = colGreen, bold = true),
      label(""),  # 空行
      # プログレスバー (count を20で割った比率を表示)
      progress(count.float, max = 20.0, fg = colBlue)
    ),

    # フッターバー: 操作説明を薄い文字で表示
    footer(" Q: Quit | SPACE: Increment ", fg = colTextMuted, bg = colBgDark)
  )

# =============================================================================
# キーイベントハンドラの登録
# =============================================================================

## スペースキー: カウンターを1加算
app.onKey(' ', proc() = inc count)

## Q キー (小文字): アプリケーションを終了
app.onKey('q', proc() = app.quit())

## Q キー (大文字): アプリケーションを終了
app.onKey('Q', proc() = app.quit())

# =============================================================================
# アプリケーション実行
# =============================================================================

## メインループを開始する
## waitFor で async 関数を同期的に実行
## build 関数を渡すことで、毎フレームウィジェットが再構築される
waitFor app.run(build)
